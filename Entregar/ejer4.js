var dia = prompt("pon la inicial del dia de la semana para ver su horario.(miercoles = i)");

switch (dia) {
    case "l":
        console.log("lunes 9:00");
        break;
        case "m":
        console.log("martes 11:00");
        break;
        case "i":
        console.log("miercoles 12:00");
        break;
        case "j":
        console.log("jueves 13:00");
        break;
        case "v":
        console.log("viernes 14:00");
        break;

    default:
        break;
}